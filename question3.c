#include<stdio.h>
int main()
{
    int x;
    printf("ENTER THE DAY NUMBER\n");
    scanf("%d",&x);
    switch(x)
    {
        case 1:printf("MONDAY IS A FIRST DAY OF A WEEK\n");
        break;
         case 2:printf("TUESDAY IS A SECOND DAY OF A WEEK\n");
        break;
         case 3:printf("WEDNESDAY IS A THIRD DAY OF A WEEK\n");
        break;
         case 4:printf("THURSDAY IS A FOURTH DAY OF A WEEK\n");
        break;
         case 5:printf("FRIDAY IS A FIVTH DAY OF A WEEK\n");
        break;
         case 6:printf("SATURDAY IS A SIXTH DAY OF A WEEK\n");
        break;
         case 7:printf("SUNDAY IS A SEVENTH DAY OF A WEEK\n");
        break;

    }

}